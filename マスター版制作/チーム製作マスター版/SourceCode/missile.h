class CMissile : public CBullet
{
public:

	CMissile(OBJ_TYPE type);			// コンストラクタ
	~CMissile();						// デストラクタ

	/* メンバ関数 */
	HRESULT						Init();								// 初期化
	void						Uninit();							// 終了
	void						Update();							// 更新
	void						Draw();								// 描画
	void						DeleteBullet();						// 弾を消す処理
	void						BulletReaction(D3DXVECTOR3 rot);	// 弾の反応
	void						DebugInfo();						// デバッグ

	/* 静的メンバ関数 */
	static	CMissile			*Create(D3DXVECTOR3 rot);			// ミサイルの生成

private:
	/* メンバ関数 */
	void						MissileRotation();					// ミサイルの回転処理
	void						MissileMove();						// ミサイルの移動処理

	/* メンバ変数 */
	D3DXVECTOR3					m_move;								// 移動量
	int							m_nCntFrame;						// フレームカウント
};
#endif