// =====================================================================================================================================================================
//
// 戦車の銃の生成
//
// =====================================================================================================================================================================
CTankGun * CTankGun::Create(D3DXVECTOR3 rot)
{
	// 変数
	CTankGun *pTankGun;

	// メモリの確保
	pTankGun = new CTankGun(OBJTYPE_BULLET);

	// 初期化
	pTankGun->Init();

	// 弾の移動量計算
	pTankGun->CalcBulletMove(rot, CGun::GUNTYPE_TANKGUN);

	//回転量保存
	pTankGun->GetRot() = rot;

	// モデルタイプの設定
	pTankGun->SetType(BULLET_MODEL);

	// モデルカウントの設定
	pTankGun->SetModelConut(MODEL_BULLET_SPHERE);

	return pTankGun;
}