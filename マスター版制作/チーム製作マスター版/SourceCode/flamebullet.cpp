void CFlameBullet::Update(void)
{
	// 更新
	CBullet::Update();

	//定期的に
	if (CBullet::GetLife() % 18 == 0)
	{
		//爆発エフェクト
		//弾に追従
		CParticle::CreateFromText(GetPosition(), ZeroVector3, CParticleParam::EFFECT_FIRE_BULLET, GetTag(), 0, WhiteColor,GetPositionPtr());
	}
}

