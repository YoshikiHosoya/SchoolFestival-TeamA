// =====================================================================================================================================================================
//
// 戦車の銃の生成
//
// =====================================================================================================================================================================
CHeliGun * CHeliGun::Create(D3DXVECTOR3 rot)
{
	// 変数
	CHeliGun *pHeliGun;

	// メモリの確保
	pHeliGun = new CHeliGun(OBJTYPE_BULLET);

	// 戦車の銃のパラメーター取得
	BULLET_PARAM *pBulletParam = pHeliGun->GetBulletParam(CGun::GUNTYPE_HELIGUN);

	// 初期化
	pHeliGun->Init();

	// 撃つ方向に合わせる
	pHeliGun->GetMove() = D3DXVECTOR3(-sinf(rot.x) * pBulletParam->fBulletSpeed,
		cosf(rot.x) * cosf(rot.y) * pBulletParam->fBulletSpeed,
		sinf(rot.y) * cosf(rot.x) * pBulletParam->fBulletSpeed);

	// モデルタイプの設定
	pHeliGun->SetType(BULLET_MODEL);

	pHeliGun->GetRot() = rot;

	// モデルカウントの設定
	pHeliGun->SetModelConut(MODEL_BULLET_SPHERE);

	return pHeliGun;
}