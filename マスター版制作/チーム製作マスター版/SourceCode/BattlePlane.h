// =====================================================================================================================================================================
// 前方宣言
// =====================================================================================================================================================================
class CGun;
// =====================================================================================================================================================================
// プレイヤーの戦車クラス
// =====================================================================================================================================================================
class CBattlePlane :public CVehicle
{
public:
	CBattlePlane(OBJ_TYPE type);							// コンストラクタ
	~CBattlePlane();										// デストラクタ

	/* メンバ関数 */
	HRESULT Init(void);										// 初期化
	void	Uninit(void);									// 終了
	void	Update(void);									// 更新
	void	Draw(void);										// 描画

	/* 静的メンバ関数 */
	static CBattlePlane	*Create();							// 生成

	/* メンバ関数 */
	void				SetPlayer(CPlayer *pPlayer) { m_pPlayer = pPlayer; };
	void				DebugInfo(void);					// デバッグ
	CGun				*GetGun() { return  m_pGun; };		// 銃のポインタ取得

private:
	/* メンバ関数 */
	void				Shot(CKeyboard *key);				// 弾を撃つ処理
	void				Operation(CKeyboard *key);			// 操作する処理
	void				Collision();						// 当たり判定をまとめてする処理
	/* メンバ変数 */
	CGun				*m_pGun;							// ガンクラスのポインタ
	CPlayer				*m_pPlayer;							// プレイヤーのポインタ
};
