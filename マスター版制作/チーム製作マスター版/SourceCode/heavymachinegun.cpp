// =====================================================================================================================================================================
//
// ヘビーマシンガンの生成
//
// =====================================================================================================================================================================
CHeavyMachinegun * CHeavyMachinegun::Create(D3DXVECTOR3 rot)
{
	// 変数
	CHeavyMachinegun *pHeavyMachinegun;

	// メモリの確保
	pHeavyMachinegun = new CHeavyMachinegun(OBJTYPE_BULLET);

	// ヘビーマシンガンのパラメーター取得
	BULLET_PARAM *pBulletParam = pHeavyMachinegun->GetBulletParam(CGun::GUNTYPE_HEAVYMACHINEGUN);

	// 初期化
	pHeavyMachinegun->Init();

	// 弾の移動量計算
	pHeavyMachinegun->CalcBulletMove(rot, CGun::GUNTYPE_HEAVYMACHINEGUN);

	// 回転量設定
	pHeavyMachinegun->GetRot() = rot;

	// モデルタイプの設定
	pHeavyMachinegun->SetType(BULLET_MODEL);

	// モデルカウントの設定
	pHeavyMachinegun->SetModelConut(MODEL_BULLET_SPHERE);

	return pHeavyMachinegun;
}