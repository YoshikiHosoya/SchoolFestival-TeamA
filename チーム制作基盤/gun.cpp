void CGun::Draw(void)
{	
	// 描画
	CModel::Draw(*m_mtx);
}

// =====================================================================================================================================================================
//
// 銃の生成
//
// =====================================================================================================================================================================
CGun * CGun::Create(D3DXVECTOR3 pos, D3DXVECTOR3 rot, D3DXMATRIX *mtx)
{
	// 変数
	CGun *pGun;

	// メモリの確保
	pGun = new CGun(OBJTYPE_MODEL);

	// 初期化
	pGun->Init();

	// マトリックス代入
	pGun->m_mtx = mtx;

	// 銃の位置の設定
	pGun->SetPosition(pos);

	// モデルタイプの設定
	pGun->SetType(2);

	// モデルカウントの設定
	pGun->SetModelConut(0);

	return pGun;
}

// =====================================================================================================================================================================
//
// 銃の種類の設定
//
// =====================================================================================================================================================================
void CGun::SetGunType(GUN_TYPE type)
{
	// 種類の設定
	m_type = type;

	// 種類ごとの弾薬数
	switch (type)
	{
	case CGun::GUNTYPE_HANDGUN:
		m_nAmmo = HANDGUN_AMMO;
		break;

	case CGun::GUNTYPE_HEAVYMACHINEGUN:
		m_nAmmo = HEAVYMACHINEGUN_AMMO;
		break;
	}
}

// =====================================================================================================================================================================
//
// 銃の発射
//
// =====================================================================================================================================================================
void CGun::Shot()
{
	switch (m_type)
	{
	case CGun::GUNTYPE_HANDGUN:
		// ハンドガンの生成
		CHandgun::Create(GetPosition(), GetRot());
		break;

	case CGun::GUNTYPE_HEAVYMACHINEGUN:
		// ヘビーマシンガンの生成
		CHeavyMachinegun::Create(GetPosition(), GetRot());
		break;
	}
}