#include "main.h"
#include "scene3D.h"

// =====================================================================================================================================================================
// バレットクラス
// =====================================================================================================================================================================
class CBullet : public CScene3D
{
public:

	CBullet();											// コンストラクタ
	~CBullet();											// デストラクタ

	/* メンバ関数 */
	HRESULT						Init();					// 初期化
	void						Uninit();				// 終了
	void						Update();				// 更新
	void						Draw();					// 描画

	/* 静的メンバ関数 */		
	static	HRESULT				Load();					// ロード
	static	void				Unload();				// アンロード

	/* メンバ関数 */
	D3DXVECTOR3					&GetMove()					{ return m_move; };		// 移動値の取得
	void						SetMove(D3DXVECTOR3 move)	{ m_move = move; };		// 移動値の設定
private:
	/* メンバ変数 */
	D3DXVECTOR3					m_move;					// 移動値
};
#endif
