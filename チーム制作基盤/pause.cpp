#include "pause.h"
#include "renderer.h"
#include "inputKeyboard.h"
#include "game.h"

CPause::PAUSE_STATE CPause::m_state = CPause::STATE_GAME;

CPause::CPause(OBJ_TYPE type) :CScene(type)
{
}

CPause::~CPause()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CPause::Init(void)
{
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CPause::Uninit(void)
{
}

//=============================================================================
// 更新処理
//=============================================================================
void CPause::Update(void)
{
	CKeyboard *key;
	key = CManager::GetInputKeyboard();


	if (GetPause() == CPause::STATE_PAUSE && key->GetKeyboardTrigger(DIK_F9))
	{
		SetPause(STATE_GAME);
	}
	else if(key->GetKeyboardTrigger(DIK_F9))
	{
		SetPause(STATE_PAUSE);
	}

}
