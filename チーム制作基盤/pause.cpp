// タイトル画面
//=============================================================================
void CPause::Draw(void)
{
}
//=============================================================================
// デバッグ
//=============================================================================
void CPause::DebugInfo(void)
{
}
//=============================================================================
// ポーズの状態設定
//=============================================================================
void CPause::SetPause(PAUSE_STATE state)
{
	m_state = state;
}
//=============================================================================
// ポーズのクリエイト
//=============================================================================
CPause* CPause::CreatePause(void)
{
	CPause*pPause;
	pPause = new CPause(OBJTYPE_PAUSE);
	pPause->Init();
	return pPause;

}
//=============================================================================
// フェードの状態取得
//=============================================================================
CPause::PAUSE_STATE CPause::GetPause(void)
{
	return m_state;
}