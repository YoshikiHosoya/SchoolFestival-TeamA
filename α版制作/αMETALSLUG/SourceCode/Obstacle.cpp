// =====================================================================================================================================================================
// 静的メンバ変数の初期化
// =====================================================================================================================================================================
OBSTACLE_PARAM	CObstacle::m_ObstacleParam[CObstacle::TYPE_MAX] = {};

// =====================================================================================================================================================================
// マクロ定義
// =====================================================================================================================================================================
#define OBSTACLE_BOX_COLLISION			(D3DXVECTOR3(50.0f,50.0f,0.0f))		// 障害物の判定の大きさ

// =====================================================================================================================================================================
//
// コンストラクタ
//
// =====================================================================================================================================================================
CObstacle::CObstacle(OBJ_TYPE type) :CModel(type)
{
	// 変数初期化
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);		// 移動値
	m_nLife = 0;								// 体力
	m_pCollision = nullptr;						// 当たり判定のポインタ
}

// =====================================================================================================================================================================
//
// デストラクタ
//
// =====================================================================================================================================================================
CObstacle::~CObstacle()
{
}

// =====================================================================================================================================================================
//
// 初期化処理
//
// =====================================================================================================================================================================
HRESULT CObstacle::Init()
{
	// タイプの初期化
	m_ObstacleType = TYPE_BOX;

	// 初期化
	CModel::Init();

	// 情報の設定
	SetObstacleParam(TYPE_BOX);

	// 当たり判定生成
	m_pCollision = CCollision::Create();
	m_pCollision->SetPos(&GetPosition());
	m_pCollision->SetSize2D(OBSTACLE_BOX_COLLISION);
	m_pCollision->SetMove(nullptr);
	m_pCollision->SetType(CCollision::OBJTYPE_OBSTACLE);
	m_pCollision->DeCollisionCreate(CCollision::COLLISIONTYPE_NORMAL);

	return S_OK;
}

// =====================================================================================================================================================================
//
// 終了処理
//
// =====================================================================================================================================================================
void CObstacle::Uninit(void)
{
	// 終了
	CModel::Uninit();
}

// =====================================================================================================================================================================
//
// 更新処理
//
// =====================================================================================================================================================================
void CObstacle::Update(void)
{
	if (m_pCollision != nullptr)
	{
		//座標の更新
		m_pCollision->SetPos(&GetPosition());
	}
	// 更新
	CModel::Update();
	CDebugProc::Print("\n 障害物　Pos %.1f, %.1f, %.1f\n", GetPosition().x, GetPosition().y, GetPosition().z);
	CDebugProc::Print(" 障害物　life %d \n", m_nLife);
}

// =====================================================================================================================================================================
//
// 描画処理
//
// =====================================================================================================================================================================
void CObstacle::Draw(void)
{
	// 描画
	CModel::Draw();
}
// =====================================================================================================================================================================
