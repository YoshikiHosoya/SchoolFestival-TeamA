#ifndef _LIGHT_H_
#define _LIGHT_H_
#define _CRT_SECURE_NO_WARNINGS

// =====================================================================================================================================================================
// インクルードファイル
// =====================================================================================================================================================================
#include "main.h"

// =====================================================================================================================================================================
// マクロ定義
// =====================================================================================================================================================================
#define MAX_LIGHT		(3)						// ライトの数

// =====================================================================================================================================================================
// ライティングクラス
// =====================================================================================================================================================================
class CLight
{
public:
	CLight();								// コンストラクタ
	~CLight();								// デストラクタ

	/* メンバ関数 */
	void	Init();							// 初期化
	void	Uninit();						// 終了
	void	Update();						// 更新

	/* 静的メンバ関数 */

private:
	/* メンバ変数 */
	D3DLIGHT9					m_aLight[MAX_LIGHT];				// ライト
	D3DXCOLOR					m_aLightCol[MAX_LIGHT] =			// ライトの拡散光を設定
		{ { D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f) },	// キーライト
		{ D3DXCOLOR(0.6f, 0.6f, 0.6f, 1.0f) },		// フィルライト
		{ D3DXCOLOR(0.2f, 0.2f, 0.2f, 1.0f) } };	// バックライト
	D3DXVECTOR3					m_aLightDir[MAX_LIGHT] =			// ライトの方向の設定
		{ { D3DXVECTOR3(0.20f, -0.80f, 0.40f) },	// キーライト
		{ D3DXVECTOR3(-0.20f, 0.80f, -0.40f) },		// フィルライト
		{ D3DXVECTOR3(0.00f, 0.00f, 1.00f) } };		// バックライト
};
#endif