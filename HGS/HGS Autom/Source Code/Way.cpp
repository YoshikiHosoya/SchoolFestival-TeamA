std::shared_ptr<CWay> CWay::Create(D3DXVECTOR3 pos)
{
	//変数宣言
	std::shared_ptr<CWay> pPlayer = std::make_shared<CWay>();


	if (pPlayer)
	{
		//初期化
		pPlayer->Init();


		return pPlayer;
	}

	//生成した情報
	return nullptr;
}

