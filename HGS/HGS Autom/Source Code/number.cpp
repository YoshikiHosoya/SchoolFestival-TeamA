D3DXVECTOR2 CNumber::m_UVsize = D3DXVECTOR2(0.0f, 0.0f);

//------------------------------------------------------------------------------
//マクロ
//------------------------------------------------------------------------------
#define ANIMATION_TIME (8)

//------------------------------------------------------------------------------
//コンストラクタ
//------------------------------------------------------------------------------
CNumber::CNumber()
{
	m_bChangeAnimation = false;
	m_nCntAnimation = 0;
	m_DefaultSize = ZeroVector3;
}
//------------------------------------------------------------------------------
//デストラクタ
//------------------------------------------------------------------------------
CNumber::~CNumber()
{
}
//------------------------------------------------------------------------------
//初期化処理
//------------------------------------------------------------------------------
HRESULT CNumber::Init()
{
	//初期化処理
	if (FAILED(CScene2D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}
//------------------------------------------------------------------------------
//終了処理
//------------------------------------------------------------------------------
void CNumber::Uninit()
{
	//終了処理
	CScene2D::Uninit();
}
//------------------------------------------------------------------------------
//更新処理
//------------------------------------------------------------------------------
void CNumber::Update()
{
	//アニメーションする時
	if (m_bChangeAnimation)
	{
		//カウンタ++
		m_nCntAnimation++;

		SetSize(m_DefaultSize + (m_DefaultSize * ((ANIMATION_TIME - m_nCntAnimation) * (1.0f / ANIMATION_TIME))));

		//アニメーションの時間に達したとき
		if (m_nCntAnimation >= ANIMATION_TIME)
		{
			m_bChangeAnimation = false;
			m_nCntAnimation = 0;
		}
	}



	//更新処理
	CScene2D::Update();
}
//------------------------------------------------------------------------------
//描画処理
//------------------------------------------------------------------------------
void CNumber::Draw()
{
	//無駄な桁数は省く
	if (GetDisp())
	{
		//描画処理
		CScene2D::Draw();
	}

}
