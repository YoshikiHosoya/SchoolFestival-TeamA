//------------------------------------------------------------------------------
//生成処理
//------------------------------------------------------------------------------
std::unique_ptr<CNumber> CNumber::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	//メモリ確保
	std::unique_ptr<CNumber> pNumber(new CNumber);

	//nullcheck
	if (pNumber)
	{

		//テクスチャ設定
		pNumber->BindTexture(CTexture::GetSeparateTexture(CTexture::SEPARATE_TEX_UI_NUMBER));
		pNumber->SetPos(pos);
		pNumber->SetSize(size);
		pNumber->m_DefaultSize = size;

		//初期化
		pNumber->Init();
		//return
		return pNumber;
	}
	return nullptr;
}

