// ------------------------------------------
//
// クラス
//
// ------------------------------------------
class CBulletinBoard : public CScene_THREE
{
public:
	/* 列挙型 */
	// タイプ
	typedef enum
	{
		TYPE_A = 0,	//
		TYPE_B,		//
		TYPE_C,		//
		TYPE_MAX
	} TYPE;

	typedef enum
	{
		STATE_EMERGENCE = 0,				// 出現
		STATE_DISAPPEARANCE					// 消失
	} STATE;

	/* 関数 */
	CBulletinBoard();
	CBulletinBoard(ACTOR actor, LAYER layer);
	~CBulletinBoard();
	void Init(void);
	void Uninit(void);
	void Update(void);
	void Draw(void);
	void SetTexture(TYPE type);							// テクスチャー設定
	void SetSize(D3DXVECTOR3 size);						// サイズ設定
	void SetPos(D3DXVECTOR3 pos);						// 位置設定
	void SetRot(D3DXVECTOR3 rot);						// 回転設定
	void SetCol(D3DXCOLOR col);							// 色設定
	void SetTex(D3DXVECTOR2 first, D3DXVECTOR2 last);	// 表示テクスチャー設定
	void SetAnim(void);									// アニメーション再生
	void Transparency(STATE state);						// 透明化
	D3DXVECTOR3 GetSize(void);							// サイズ取得
	D3DXVECTOR3 GetRot(void);							// 回転取得
	D3DXVECTOR3 GetPos(void);							// 位置取得
	static CBulletinBoard * Create();					// 作成
	static void LoadCreate(void);						// アイテム生成
	static HRESULT Load(void);							// 読み込み
	static void UnLoad(void);							// 破棄
	static CLoad *GetLoad(void);						// ロード取得
protected:
private:
	static LPDIRECT3DTEXTURE9 m_pTexture[TYPE_MAX];		// テクスチャへのポインタ
	static CLoad	*m_pload;		// ロード
	static int		m_nAll;			// 番号
	TYPE			m_Type;			// タイプ
	float			m_fLengh;		// 長さ
	float			m_fAngle;		// 角度
	D3DXCOLOR		m_col;			// 色
	int				m_nCntAnim;		// 1フレームあたりのカウント
	D3DXVECTOR2		m_TexFirst;		// テクスチャ座標の目標
	D3DXVECTOR2		m_TexLast;		// テクスチャ座標の目標
	D3DXVECTOR2		m_TexFirstSign;	// テクスチャ座標
	D3DXVECTOR2		m_TexLastSign;	// テクスチャ座標
};
#endif